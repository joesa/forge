"""
DB Agent (Agent 6) — generates database schema, models, migrations.

Layer 2 inject: Pydantic schemas from pydantic_schema_injector.
Layer 9: migration_safety.py checks for destructive operations.
After this agent: db_type_injector generates TypeScript types for agents 3+4.
Files: prisma/schema.prisma OR alembic models, migration files.
Gate G7 after.
"""

from __future__ import annotations

import json
import time
from typing import Any

import structlog

from app.agents.ai_router import AIRouter
from app.agents.build.context_window_manager import ContextWindowManager
from app.agents.state import PipelineState

logger = structlog.get_logger(__name__)

AGENT_NAME = "db"

_SYSTEM_PROMPT = """You are a senior database engineer generating database schema and models.
Generate schema definitions based on the Pydantic schemas and project requirements.

Return a JSON object where keys are file paths and values are file contents.
Requirements:
- Define all database models with proper types, relations, and indexes
- Include migration files or schema definition
- Use Prisma for TypeScript projects or SQLAlchemy for Python backends
- Set up proper cascading deletes and foreign key constraints
- Include seed data types for testing"""


def _extract_entities(state: PipelineState) -> list[dict[str, Any]]:
    """Extract entity definitions from plan or schemas."""
    plan = state.get("comprehensive_plan", {})
    entities: list[dict[str, Any]] = []

    db_design = plan.get("db_design", plan.get("database", {}))
    if isinstance(db_design, dict):
        entity_list = db_design.get("entities", db_design.get("models", []))
        if isinstance(entity_list, list):
            for e in entity_list:
                if isinstance(e, dict):
                    entities.append(e)

    if not entities:
        entities = [
            {"name": "User", "fields": [
                {"name": "id", "type": "uuid", "primary": True},
                {"name": "email", "type": "string", "unique": True},
                {"name": "name", "type": "string"},
                {"name": "passwordHash", "type": "string"},
                {"name": "createdAt", "type": "datetime"},
                {"name": "updatedAt", "type": "datetime"},
            ]},
            {"name": "Session", "fields": [
                {"name": "id", "type": "uuid", "primary": True},
                {"name": "userId", "type": "uuid", "relation": "User"},
                {"name": "token", "type": "string", "unique": True},
                {"name": "expiresAt", "type": "datetime"},
            ]},
        ]

    return entities


def _build_default_db(state: PipelineState) -> dict[str, str]:
    """Build deterministic default database schema files."""
    idea = state.get("idea_spec", {})
    title = str(idea.get("title", "app"))
    framework = str(idea.get("framework", "react_vite"))
    entities = _extract_entities(state)
    files: dict[str, str] = {}

    # Prisma schema for TypeScript projects
    schema_lines = [
        "// Auto-generated by FORGE db_agent",
        "generator client {",
        '  provider = "prisma-client-js"',
        "}",
        "",
        "datasource db {",
        '  provider = "postgresql"',
        '  url      = env("DATABASE_URL")',
        "}",
        "",
    ]

    for entity in entities:
        name = entity.get("name", "Model")
        fields_def = entity.get("fields", [])
        schema_lines.append(f"model {name} {{")

        for field in fields_def:
            if not isinstance(field, dict):
                continue
            fname = field.get("name", "field")
            ftype = field.get("type", "string")

            prisma_type = {
                "uuid": "String @id @default(uuid())",
                "string": "String",
                "int": "Int",
                "float": "Float",
                "boolean": "Boolean",
                "datetime": "DateTime @default(now())",
                "json": "Json",
            }.get(ftype, "String")

            if field.get("primary"):
                prisma_type = "String @id @default(uuid())"
            elif field.get("unique"):
                prisma_type += " @unique"
            elif field.get("optional"):
                prisma_type += "?"

            schema_lines.append(f"  {fname} {prisma_type}")

        schema_lines.append("}")
        schema_lines.append("")

    files["prisma/schema.prisma"] = "\n".join(schema_lines) + "\n"

    # DB client utility
    files["src/lib/db.ts"] = (
        "import { PrismaClient } from '@prisma/client';\n\n"
        "const globalForPrisma = globalThis as unknown as { prisma: PrismaClient };\n\n"
        "export const prisma = globalForPrisma.prisma ?? new PrismaClient({\n"
        "  log: process.env.NODE_ENV === 'development' ? ['query'] : [],\n"
        "});\n\n"
        "if (process.env.NODE_ENV !== 'production') {\n"
        "  globalForPrisma.prisma = prisma;\n"
        "}\n\n"
        "export default prisma;\n"
    )

    # DB types
    type_lines = ["// Auto-generated TypeScript types from DB schema\n"]
    for entity in entities:
        name = entity.get("name", "Model")
        type_lines.append(f"export interface {name} {{")
        for field in entity.get("fields", []):
            if not isinstance(field, dict):
                continue
            fname = field.get("name", "field")
            ftype = field.get("type", "string")
            ts_type = {"uuid": "string", "string": "string", "int": "number",
                        "float": "number", "boolean": "boolean",
                        "datetime": "Date", "json": "Record<string, unknown>"}.get(ftype, "string")
            opt = "?" if field.get("optional") else ""
            type_lines.append(f"  {fname}{opt}: {ts_type};")
        type_lines.append("}\n")

    files["src/types/db.ts"] = "\n".join(type_lines)

    return files


async def run_db_agent(
    state: PipelineState,
    ai_router: AIRouter,
    context_window_manager: ContextWindowManager,
) -> dict[str, str]:
    """Generate DB schema and models. temp=0, Pydantic schema injection."""
    start = time.monotonic()
    idea = state.get("idea_spec", {})
    injected = state.get("injected_schemas", {})

    pydantic_ctx = ""
    if "pydantic_schemas" in injected:
        pydantic_ctx = f"\nPydantic Schemas:\n{str(injected['pydantic_schemas'])[:3000]}\n"

    user_prompt = (
        f"Project: {idea.get('title', 'Untitled')}\n"
        f"Description: {idea.get('description', '')}\n"
        f"{pydantic_ctx}"
    )

    try:
        files = await context_window_manager.generate(
            system_prompt=_SYSTEM_PROMPT, user_prompt=user_prompt, agent_name=AGENT_NAME,
        )
        if files:
            logger.info("db_agent.complete", elapsed_s=round(time.monotonic() - start, 3), files=len(files))
            return files
    except Exception as exc:
        logger.warning("db_agent.llm_fallback", error=str(exc))

    files = _build_default_db(state)
    logger.info("db_agent.complete_default", elapsed_s=round(time.monotonic() - start, 3), files=len(files))
    return files
